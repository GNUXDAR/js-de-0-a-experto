'use strict'

//Bucle do while
/* Bucle es una estructura de control que se repite N veces
el do while se ejecutara primeramente y despues evalua mediante la condicion
y se repetira si la condicion se cumple
si no se cumple sale del bucle
*/

 var age = 20;

 do{
 	console.log('Tienes de edad: '+age);
 	age++;
 }while(age <= 25);